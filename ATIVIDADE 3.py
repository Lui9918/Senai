# Faça um programa que peça dois numeros e imprima a soma.

if __name__ == '__main__':
    # Pedindo o primeiro numero
    print("Write first number:")
    numero1 = int(input())# int() converte uma string para inteiro

    # Pedindo o segundo numero
    print("Write second number:")
    numero2 = int(input())

    # Fazendo a soma
    resultado = numero1 + numero2
    # Imprimindo
    print(f'Result: {resultado}')

    # Imprimindo
